"use client";

import { motion, AnimatePresence } from "framer-motion";
import { Button } from "@/components/ui/Button";
import { X, Crown, Zap, Lock } from "lucide-react";
import { useAuth } from "@/lib/auth-context";
import { useLanguage } from "@/lib/i18n";
import Link from "next/link";
import { useState } from "react";

interface UpgradeModalProps {
    isOpen: boolean;
    onClose: () => void;
    type: "login" | "upgrade";
    scansUsed?: number;
    maxScans?: number;
}

export function UpgradeModal({ isOpen, onClose, type, scansUsed = 0, maxScans = 2 }: UpgradeModalProps) {
    const { t } = useLanguage();
    const { signInWithGoogle } = useAuth();
    const [isSigningIn, setIsSigningIn] = useState(false);

    const handleGoogleLogin = async () => {
        setIsSigningIn(true);
        try {
            await signInWithGoogle();
            onClose();
        } catch (error) {
            console.error("Login failed:", error);
        } finally {
            setIsSigningIn(false);
        }
    };

    return (
        <AnimatePresence>
            {isOpen && (
                <>
                    {/* Backdrop */}
                    <motion.div
                        initial={{ opacity: 0 }}
                        animate={{ opacity: 1 }}
                        exit={{ opacity: 0 }}
                        onClick={onClose}
                        className="fixed inset-0 bg-black/80 backdrop-blur-sm z-50"
                    />

                    {/* Modal */}
                    <motion.div
                        initial={{ opacity: 0, scale: 0.95, y: 20 }}
                        animate={{ opacity: 1, scale: 1, y: 0 }}
                        exit={{ opacity: 0, scale: 0.95, y: 20 }}
                        transition={{ type: "spring", duration: 0.5 }}
                        className="fixed left-1/2 top-1/2 -translate-x-1/2 -translate-y-1/2 z-50 w-full max-w-md"
                    >
                        <div className="relative bg-[#0A0A0A] border border-white/10 rounded-2xl shadow-2xl overflow-hidden">
                            {/* Glow Effect */}
                            <div className="absolute -top-20 -left-20 w-40 h-40 bg-purple-500/20 blur-[60px] rounded-full pointer-events-none" />
                            <div className="absolute -bottom-20 -right-20 w-40 h-40 bg-indigo-500/20 blur-[60px] rounded-full pointer-events-none" />

                            {/* Close Button */}
                            <button
                                onClick={onClose}
                                className="absolute top-4 right-4 p-2 rounded-lg hover:bg-white/10 transition-colors z-10"
                            >
                                <X className="w-5 h-5 text-gray-400" />
                            </button>

                            <div className="relative p-8 text-center">
                                {/* Icon */}
                                <div className="mb-6 flex justify-center">
                                    <div className={`p-4 rounded-2xl ${type === "login" ? "bg-blue-500/10 border border-blue-500/20" : "bg-purple-500/10 border border-purple-500/20"}`}>
                                        {type === "login" ? (
                                            <Lock className="w-10 h-10 text-blue-400" />
                                        ) : (
                                            <Crown className="w-10 h-10 text-purple-400" />
                                        )}
                                    </div>
                                </div>

                                {/* Title */}
                                <h3 className="text-2xl font-bold text-white mb-3">
                                    {type === "login" ? t.upgradeModal.loginTitle : t.upgradeModal.upgradeTitle}
                                </h3>

                                {/* Description */}
                                <p className="text-gray-400 mb-2">
                                    {type === "login" ? (
                                        <span dangerouslySetInnerHTML={{
                                            __html: t.upgradeModal.loginDesc
                                                .replace("{used}", `<span class="text-white font-semibold">${scansUsed}</span>`)
                                                .replace("{max}", `<span class="text-white font-semibold">${maxScans}</span>`)
                                        }} />
                                    ) : (
                                        <span dangerouslySetInnerHTML={{
                                            __html: t.upgradeModal.upgradeDesc
                                                .replace("{max}", `<span class="text-white font-semibold">${maxScans}</span>`)
                                        }} />
                                    )}
                                </p>
                                <p className="text-gray-500 text-sm mb-8">
                                    {type === "login"
                                        ? t.upgradeModal.loginSubDesc
                                        : t.upgradeModal.upgradeSubDesc
                                    }
                                </p>

                                {/* Actions */}
                                {type === "login" ? (
                                    <Button
                                        onClick={handleGoogleLogin}
                                        disabled={isSigningIn}
                                        className="w-full bg-white text-black hover:bg-gray-100 font-semibold py-3 rounded-xl flex items-center justify-center gap-2"
                                    >
                                        <svg className="w-5 h-5" viewBox="0 0 24 24">
                                            <path
                                                fill="currentColor"
                                                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                            />
                                            <path
                                                fill="currentColor"
                                                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                            />
                                            <path
                                                fill="currentColor"
                                                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                            />
                                            <path
                                                fill="currentColor"
                                                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                            />
                                        </svg>
                                        {isSigningIn ? t.upgradeModal.loggingIn : t.upgradeModal.googleLogin}
                                    </Button>
                                ) : (
                                    <div className="space-y-3">
                                        <Link href="/pricing" onClick={onClose}>
                                            <Button className="w-full bg-linear-to-r from-purple-600 to-pink-600 hover:shadow-[0_0_30px_rgba(168,85,247,0.4)] font-semibold py-3 rounded-xl flex items-center justify-center gap-2 text-white">
                                                <Crown className="w-5 h-5" />
                                                {t.upgradeModal.viewPlans}
                                            </Button>
                                        </Link>
                                        <p className="text-gray-500 text-xs">
                                            {t.upgradeModal.startFrom}
                                        </p>
                                    </div>
                                )}

                                {/* Benefits */}
                                <div className="mt-8 pt-6 border-t border-white/10">
                                    <p className="text-gray-500 text-xs mb-4">
                                        {type === "login" ? t.upgradeModal.freeBenefitsTitle : t.upgradeModal.proBenefitsTitle}
                                    </p>
                                    <div className="grid grid-cols-2 gap-3 text-left">
                                        {(type === "login" ? t.upgradeModal.freeBenefits : t.upgradeModal.proBenefits).map((benefit, i) => (
                                            <div key={i} className="flex items-center gap-2 text-gray-400 text-xs">
                                                <Zap className="w-3 h-3 text-purple-400" />
                                                {benefit}
                                            </div>
                                        ))}
                                    </div>
                                </div>
                            </div>
                        </div>
                    </motion.div>
                </>
            )}
        </AnimatePresence>
    );
}
